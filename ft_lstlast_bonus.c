#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*k;

	k = lst;
	while (k)
	{
		if (!(k -> next))
		{
			return (k);
		}
		k = k -> next;
	}
	return (k);
}
